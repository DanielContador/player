        public ParsedScorm(String data, String version) {
            switch (version)
            {
                case "1.2":
                    parse12(data);
                    break;
                case "CAM 1.3":
                    parse2004(data);
                    break;
                case "2004 3rd Edition":
                    parse2004(data);
                    break;
                case "2004 4th Edition":
                    parse2004(data);
                    break;
            }
        }
        public ParsedScorm(Sco message)
        {
            switch (message.getVersion()){
                case "1.2":
                    parse12(message.getTheData());
                    break;
                case "CAM 1.3":
                    parse2004(message.getTheData());
                    break;
                case "2004 3rd Edition":
                    parse2004(message.getTheData());
                    break;
                case "2004 4th Edition":
                    parse2004(message.getTheData());
                    break;
            }


        }

